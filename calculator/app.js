class Calculator {
    static add(a, b) {
        return a + b
    }
    static substract(a, b) {
        return a - b
    }
    static multiply(a, b) {
        return a * b
    }
    static divide(a, b) {
        return a / b
    }
}

console.log(`9 + 8 = ${Calculator.add(9, 8)}`)
console.log(`9 - 8 = ${Calculator.substract(9, 8)}`)
console.log(`9 * 8 = ${Calculator.add(9, 8)}`)
console.log(`9 / 8 = ${Calculator.add(9, 8)}`)